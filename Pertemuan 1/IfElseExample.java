/*
 Program berikut untuk mencoba seleksi if-else
*/ 

public class IfElseExample { 

 public static void main(String[] args) {

  int number=13;  
  if(number%2==0){  
  	System.out.println("Angka genap");  
  }
  else{  
   System.out.println("Angka ganjil");  
  }

    
 }  
}   

/*
	program di atas akan dilakukan pengecekan apakah angka 13 merupakan
	bilangan genap atau ganjil 
	angka 13 adalah bilangan ganjil karena jika dibagi 2 tidak menghasilkan
	sisa nol, maka akan dicetak "Angka ganjil"
  */